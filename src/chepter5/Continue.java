package chepter5;

public class Continue {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
            if (i % 2 == 0) {
                continue;
            }
            System.out.println("");
        }

        int i = 10;
        int j = 25;
        int k = 200;
        int a = 8;
        int b = 18;
        int c = 5;
        int d = 0;
    }
}
